CREATE TABLE ANOMALY_RESULTS (
    ID             BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    SOURCE_TABLE   NVARCHAR(20) NOT NULL,
    SOURCE_ID      BIGINT NOT NULL,
    IS_ANOMALY     BOOLEAN NOT NULL DEFAULT FALSE,
    ANOMALY_SCORE  DOUBLE,
    ANOMALY_TYPE   NVARCHAR(50),
    CLUSTER_ID     INTEGER,
    ML_MODEL_VER   NVARCHAR(20) DEFAULT 'v1',
    DETECTED_AT    TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IDX_ANOMALY_IS_ANOMALY  ON ANOMALY_RESULTS (IS_ANOMALY);
CREATE INDEX IDX_ANOMALY_DETECTED_AT ON ANOMALY_RESULTS (DETECTED_AT DESC);
CREATE INDEX IDX_ANOMALY_SOURCE      ON ANOMALY_RESULTS (SOURCE_TABLE, SOURCE_ID);
